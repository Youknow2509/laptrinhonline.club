
#include <bits/stdc++.h>

using namespace std;

int main(){
    long long a, b;
    cin >> a >> b;
    cout << a << "+" << b << "=" << (long long)a+b << endl;
    cout << a << "-" << b << "=" << (long long)a-b << endl;
    cout << a << "*" << b << "=" << (long long)a*b << endl;
    cout << a << "/" << b << "=" << (long long)a/b << endl;
    cout << a << "%" << b << "=" << (long long)a%b << endl;
    return 0;   
}